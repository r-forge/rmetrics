      DOUBLE PRECISION AZ(*),EPS7,UMAX
      DOUBLE PRECISION TEMP
      INTEGER NAA,J,K,L
      IOLD=0
      UMAX=0.0D 0
      NAA=NF-N
      DO 2 J=1,NAA
      TEMP=AZ(J)
